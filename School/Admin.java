        String Id_Number=jTextField1.getText();
        String Password=jPasswordField1.getText();
        try{
            Class.forName("java.sql.DriverManager");
            Connection con=(Connection)
                    DriverManager.getConnection("jdbc:mysql://localhost:3306/Schoolmanagement","root","Mp@0509");
            Statement st=(Statement) con.createStatement();
            String query= "select* from Account where Id_Number='"+Id_Number+"' and Password='"+Password+"'";
            ResultSet rs=st.executeQuery(query);
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this,"LoginSuccessfully");
                Schoolfront sf=new Schoolfront();
                sf.show();
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Please Enter the Correct Username and Password");
                jTextField1.setText("");
                jPasswordField1.setText("");
            }
        }
        catch(Exception e){
           System.out.println(e);
        }
